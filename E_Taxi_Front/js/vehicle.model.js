class Vehicle{
    constructor(
        id,
        manufacturer,
        model,
        vin,
        yearOfProduction,
        licensePlate,
        isServiced,
        nextService,
        isActive,
    ){
        this.id=id;
        this.manufacturer=manufacturer;
        this.model=model;
        this.vin=vin;
        this.yearOfProduction=yearOfProduction;
        this.licensePlate=licensePlate;
        this.isServiced=isServiced;
        this.nextService=nextService;
        this.isActive=isActive;

    }


}